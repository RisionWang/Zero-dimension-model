%% CO2 heat capacity J/(mol*K)
function Cp_CO2=Cp_CO2(T) % K
         t=T./1000;
         if T<1200
         Cp_CO2=24.99735+55.18696*t+(-33.69137)*t.^2+7.948387*t.^3+(-0.136638)./t.^2;
         else
         Cp_CO2=58.16639+2.720074*t+(-0.492289)*t.^2+0.038844*t.^3+(-6.447293)./t.^2;
         end
end